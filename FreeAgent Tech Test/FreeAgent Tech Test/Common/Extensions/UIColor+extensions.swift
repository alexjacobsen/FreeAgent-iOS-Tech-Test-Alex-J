extension UIColor {
    
    class func freeAgentBlue() -> UIColor { return  #colorLiteral(red: 0, green: 0.5571982265, blue: 0.7953300476, alpha: 1) }
    class func freeAgentGreen() -> UIColor { return  #colorLiteral(red: 0.430282414, green: 0.5921179652, blue: 0.1319748163, alpha: 1) }
    class func freeAgentGrey() -> UIColor { return  #colorLiteral(red: 0.7831789851, green: 0.7785247564, blue: 0.786757648, alpha: 1) }
    class func freeAgentLightGrey() -> UIColor { return  #colorLiteral(red: 0.8892249465, green: 0.8839392066, blue: 0.8932881951, alpha: 1) }
}
